"""Markdown report generation for VacuumForge.

Generates human-readable reports for validations, derivations,
failures, and model summaries.
"""

from __future__ import annotations

from typing import TYPE_CHECKING, Any

import sympy

if TYPE_CHECKING:
    from vacuumforge.core.context import TheoryContext
    from vacuumforge.requirements.validators import ValidationResult


def md_math(expr: sympy.Basic) -> str:
    """Render a SymPy expression as a markdown math block."""
    return f"$$\n{sympy.latex(expr)}\n$$"


def md_inline_math(expr: sympy.Basic) -> str:
    """Render a SymPy expression as inline math."""
    return f"${sympy.latex(expr)}$"


