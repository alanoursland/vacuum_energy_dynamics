def case_1_derivative_classification(out: ScriptOutput):
    header("Case 1: Local two-derivative first-order derivative classes")
    statuses = {}
    for item in DERIVATIVE_CLASSES:
        deg = degree(item["expr"])
        statuses[item["name"]] = item["status"]
        print(f"  {item['name']:<10} degree={deg} status={item['status']}")
        print(f"    {item['reason']}")

    expected_open = {"H2.Rlin", "H2.Q"}
    actual_open = {name for name, status in statuses.items() if status == "open_unless_reduced"}
    discharged = {
        name
        for name, status in statuses.items()
        if status in {"boundary", "free_core", "generated"}
    }
    expected_discharged = {"eta.Rlin", "H.Rlin", "eta.Q", "H.Q"}

    ok = actual_open == expected_open and discharged == expected_discharged
    with out.derived_results():
        out.line(
            "derivative-class audit isolates only higher-H non-Palatini terms",
            StatusMark.PASS if ok else StatusMark.FAIL,
            "eta.Rlin/H.Rlin/eta.Q/H.Q are classified; H2.Rlin and H2.Q are the remaining ansatz-reduction targets",
        )
    return ok


def case_2_universal_replacement_vs_open_terms(out: ScriptOutput):
    header("Case 2: Universal replacement misses exactly the open derivative terms")
    replacement = monomial(0, "Rlin") + monomial(1, "Rlin") + monomial(0, "Q") + monomial(1, "Q")
    candidate_space = sum(item["expr"] for item in DERIVATIVE_CLASSES)
    open_terms = monomial(2, "Rlin") + monomial(2, "Q")
    residual = sp.simplify(candidate_space - replacement - open_terms)

    print("candidate derivative space through H^2:")
    print("  eta.Rlin + H.Rlin + eta.Q + H.Q + H2.Rlin + H2.Q")
    print("universal replacement derivative output:")
    print("  eta.Rlin + H.Rlin + eta.Q + H.Q")
    print(f"  residual after subtracting open terms = {sp.sstr(residual)}")

    ok = is_zero(residual)
    with out.derived_results():
        out.line(
            "universal replacement leaves only H2.Rlin and H2.Q as unresolved derivative classes",
            StatusMark.PASS if ok else StatusMark.FAIL,
            "the remaining proof must reduce or exclude those classes",
        )
    return ok


def case_3_scope_and_residuals(out: ScriptOutput):
    header("Case 3: Scope exclusions and admitted residual")
    for item in NON_DERIVATIVE_CLASSES:
        degs = [degree(term) for term in sp.Add.make_args(item["expr"])]
        print(f"  {item['name']}: status={item['status']} degree-samples={degs}")
        print(f"    {item['reason']}")

    for name, reason in SCOPE_EXCLUSIONS:
        print(f"  {name}: excluded")
        print(f"    {reason}")

    status_counts = {
        "admitted": len(NON_DERIVATIVE_CLASSES),
        "excluded": len(SCOPE_EXCLUSIONS),
    }
    ok = status_counts == {"admitted": 1, "excluded": 4}
    with out.governance_assessments():
        out.line(
            "non-derivative residual and scope exclusions recorded",
            StatusMark.PASS if ok else StatusMark.FAIL,
            "Lambda admitted; higher derivatives, extra fields, torsion/nonmetricity, and nonuniversal coupling are outside this closure theorem",
        )
    return ok


