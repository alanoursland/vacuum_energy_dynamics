ARCHIVE_ROOT = Path(__file__).resolve().parents[1] / ".vacuumforge_archive"
SCRIPT_ID = f"{Path(__file__).parent.name}__{Path(__file__).stem}"


def header(title: str) -> None:
    print()
    print("=" * 120)
    print(title)
    print("=" * 120)


def is_zero(expr) -> bool:
    try:
        return bool(sp.simplify(expr) == 0)
    except Exception:
        return False


def print_archive_status(ns, invalidated: bool) -> None:
    if invalidated:
        print("[INFO] Archive invalidated due to source change.")
    checks = ns.verify_dependencies()
    if not checks:
        print("[INFO] Archive dependencies: none declared.")
        return
    print("[INFO] Archive dependency check:")
    for check in checks:
        print(f"  - {check.dependency.dependency_id}: {check.status} ({check.message})")


def prepare_archive():
    archive = ProjectArchive(ARCHIVE_ROOT)
    ns = archive.script_namespace(SCRIPT_ID)
    invalidated = ns.check_source_invalidation(__file__)
    ns.declare_dependency(
        dependency_id="closure_step_4_dependency_018",
        upstream_script_id="018_closure_uniqueness__closure_step_4_connection_elimination",
        upstream_derivation_id="palatini_connection_uniqueness_018",
        expected_record_kind=RecordKind.DERIVATION,
    )
    return archive, ns, invalidated


H, R, Q, LAM = sp.symbols("H Rlin Q Lambda")


def monomial(h_power: int, operator: str):
    if operator == "Rlin":
        return H**h_power * R
    if operator == "Q":
        return H**h_power * Q
    if operator == "Lambda":
        return H**h_power * LAM
    raise ValueError(operator)


def degree(expr):
    powers = sp.Poly(expr, H, R, Q, LAM).monoms()[0]
    return {
        "H": powers[0],
        "Rlin": powers[1],
        "Q": powers[2],
        "Lambda": powers[3],
    }


