def header(title: str) -> None:
    print()
    print("=" * 116)
    print(title)
    print("=" * 116)


def is_zero(expr) -> bool:
    try:
        return bool(sp.simplify(expr) == 0)
    except Exception:
        return False


def prepare_archive():
    archive = ProjectArchive(ARCHIVE_ROOT)
    ns = archive.script_namespace(SCRIPT_ID)
    invalidated = ns.check_source_invalidation(__file__)
    ns.declare_dependency(
        dependency_id="trace_free_spatial_shear_inventory",
        upstream_script_id="005_nonspherical_sectors__candidate_nonspherical_degree_inventory",
        upstream_derivation_id="trace_free_spatial_shear_inventory",
    )
    return archive, ns, invalidated


def print_archive_status(ns, invalidated: bool) -> None:
    if invalidated:
        print("[INFO] Archive invalidated due to source change.")
    checks = ns.verify_dependencies()
    if not checks:
        print("[INFO] Archive dependencies: none declared.")
        return
    print("[INFO] Archive dependency check:")
    for check in checks:
        print(f"  - {check.dependency.dependency_id}: {check.status} ({check.message})")


def curl_cartesian(V, x, y, z):
    Vx, Vy, Vz = V
    return sp.Matrix([
        sp.diff(Vz, y) - sp.diff(Vy, z),
        sp.diff(Vx, z) - sp.diff(Vz, x),
        sp.diff(Vy, x) - sp.diff(Vx, y),
    ])


