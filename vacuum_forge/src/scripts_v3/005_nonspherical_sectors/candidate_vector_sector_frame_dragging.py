# Case 4: Dipole-like exterior vector potential
# =============================================================================

def case_4_dipole_vector_potential(ns):
    header("Case 4: Dipole-like exterior vector potential")

    x, y, z, J = sp.symbols("x y z J", real=True)
    r = sp.sqrt(x**2 + y**2 + z**2)

    # Choose angular momentum along z: J_vec = J zhat.
    # A dipole-like vector potential proportional to J x r / r^3.
    # J_vec x r_vec = (-J y, J x, 0)
    W = sp.Matrix([
        -J*y/r**3,
        J*x/r**3,
        0
    ])

    curlW = sp.simplify(curl_cartesian(W, x, y, z))

    print("For angular momentum along z:")
    print("  J_vec x r_vec = (-J y, J x, 0)")
    print()
    print("Candidate exterior vector potential:")
    print(f"W = {W}")
    print()
    print("curl(W) =")
    print(curlW)

    curl_nonzero = not all(is_zero(c) for c in curlW)
    out = ScriptOutput()
    with out.derived_results():
        out.line("dipole-like vector potential has nonzero curl",
                 StatusMark.PASS if curl_nonzero else StatusMark.FAIL,
                 f"curl W = {curlW}")

    ns.record_derivation(
        derivation_id="dipole_vector_potential_curl",
        inputs=[W[0], W[1], W[2]],
        output=curlW,
        method="curl_cartesian_dipole_vector_potential",
        status=Status.DERIVED,
        record_kind=RecordKind.DIAGNOSTIC_EXAMPLE,
        scope="dipole-like vector potential for J along z",
    )


