    header("Case 1: Scalar metric has no g_ti")

    psi = sp.symbols("psi", real=True)

    # Weak scalar metric perturbation in block form:
    # h_tt = -2 psi (up to sign convention), h_ti=0, h_ij=-2psi delta_ij.
    h_ti = sp.Matrix([[0, 0, 0]])

    print("Weak scalar metric has:")
    print(f"  h_ti = {h_ti}")
    print()
    print("Therefore scalar A cannot represent frame dragging.")
    status_line("scalar sector has zero vector components", True)


