# Purpose
# -------
# The nonspherical degree inventory shows that scalar A handles the weak
# Newtonian scalar sector, but cannot produce off-diagonal components:
#
#   g_ti != 0
#
# These are needed for rotating sources, gravitomagnetism, and frame dragging.
#
# This script tests a reduced weak vector-sector candidate:
#
#   g_ti ~ vector potential W_i
#
# analogous to a weak gravitomagnetic vector potential.
#
# It does not derive the full GR frame-dragging equation. It checks:
#
#   1. scalar A cannot create g_ti,
#   2. a vector W_i is a distinct sector,
#   3. rotational sources require an angular-momentum-like source,
#   4. exterior dipole-like vector potential carries angular momentum,
#   5. curl W gives a frame-dragging-like field,
#   6. this sector must be added beyond scalar A/kappa.
#
# Suggested location:
#   scripts_v3/candidate_vector_sector_frame_dragging.py

import sympy as sp


# =============================================================================
# Utilities
# =============================================================================

def header(title: str) -> None:
    print()
    print("=" * 116)
    print(title)
    print("=" * 116)


def status_line(label: str, ok: bool, detail: str = "") -> None:
    mark = "PASS" if ok else "WARN"
    if detail:
        print(f"[{mark}] {label}: {detail}")
    else:
        print(f"[{mark}] {label}")


def is_zero(expr) -> bool:
    try:
        return bool(sp.simplify(expr) == 0)
    except Exception:
        return False


def curl_cartesian(V, x, y, z):
    Vx, Vy, Vz = V
    return sp.Matrix([
        sp.diff(Vz, y) - sp.diff(Vy, z),
        sp.diff(Vx, z) - sp.diff(Vz, x),
        sp.diff(Vy, x) - sp.diff(Vx, y),
    ])


